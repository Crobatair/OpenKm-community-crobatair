package com.openkm.crobatair.adapter;

public class CrobatairAdapterException extends Exception {
	private static final long serialVersionUID = 1L;

	public CrobatairAdapterException() {
		super();
	}

	public CrobatairAdapterException(String arg0) {
		super(arg0);
	}

	public CrobatairAdapterException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public CrobatairAdapterException(Throwable arg0) {
		super(arg0);
	}
}
